package Phase1;

class Log {

    void logError(String str) {
        System.out.println("Error-> " + str);
    }


    void logInfo(String str) {
        System.out.println("Info-> " + str);
    }


    void logCatchExe(String str) {
        System.out.println("Catch Exception-> " + str);
    }

    void log(String str) {
        System.out.println(str);
    }

}
